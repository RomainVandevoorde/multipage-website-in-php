            Les Centres d’Action Sociale Globale s’adressent à toute personne confrontée à une difficulté sociale qui en fait la demande.</br></br>
                <b>Quelles sont leurs missions ?</b></br></br>
            Les CASG développent leur action sociale globale en assurant aux bénéficiaires :</br>
            <ul>
              <li>un premier accueil</li>
              <li>une analyse de leur situation</li>
              <li>un accompagnement social</li>
              <li>si nécessaire, une orientation vers un service spécialisé en fonction de la problématique</li>
            </ul>
            En outre, les CASG relayent aux autorités publiques les problèmes et les lacunes qui se font jour dans la collectivité.</br></br>
                <b>Quels sont leurs modes d’intervention ?</b></br></br>
